    private void loadDiskon() {
        DefaultTableModel modelDiscount = new DefaultTableModel(null, Diskon.DISKON_COLUMN_TITLE);
        modelDiscount.setRowCount(0);

        String[][] listDiskon = diskon.getListDiskon();
        for (String[] data : listDiskon) {
            modelDiscount.addRow(data);
        }

        tbDiscount.setModel(modelDiscount);
    }

    private void initDateFormat() {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        dpMulai.setDateFormat(sdf);
        dpAkhir.setDateFormat(sdf);
    }

    private void clearInput() {
        tfNamaPromo.setText("");
        tfJenisPromo.setText("");
        spDiscount.setValue(0);
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        dpMulai.setText(sdf.format(new Date()));
        dpAkhir.setText(sdf.format(new Date()));
    }

