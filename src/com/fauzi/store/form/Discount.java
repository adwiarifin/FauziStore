    private void btSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSubmitActionPerformed
        String nama = tfNamaPromo.getText().trim();
        String jenis = tfJenisPromo.getText().trim();
        int besarDiskon = Integer.parseInt(spDiscount.getValue().toString());
        String mulai = dpMulai.getText();
        String akhir = dpAkhir.getText();

        if (nama.isEmpty()
                || jenis.isEmpty()
                || besarDiskon == 0) {
            JOptionPane.showMessageDialog(rootPane, "Pastikan semua masukan sudah terisi!", "Invalid Input", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        if (diskon.insertDiskon(nama, jenis, besarDiskon, mulai, akhir)) {
            // informasikan jika insert berhasil
            JOptionPane.showMessageDialog(rootPane, "Data diskon baru berhasil dimasukkan", "Info", JOptionPane.INFORMATION_MESSAGE);
            // reload diskon
            loadDiskon();
            // siapkan untuk input baru
            clearInput();
            tfNamaPromo.requestFocus();
        } else {
            // informasikan jika insert gagal
            JOptionPane.showMessageDialog(rootPane, "Data diskon gagal dimasukkan", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btSubmitActionPerformed

